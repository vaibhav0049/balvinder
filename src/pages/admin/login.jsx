export default function Login(props) {
  const [emailError, setEmailError] = React.useState(false);
  const [emailErrorMessage, setEmailErrorMessage] = React.useState('');
  const [passwordError, setPasswordError] = React.useState(false);
  const [passwordErrorMessage, setPasswordErrorMessage] = React.useState('');
  const [formErrorMessage, setFormErrorMessage] = React.useState(''); // <-- Add this line
  const navigate = useNavigate();

  const validateInputs = () => {
    const email = document.getElementById('email');
    const password = document.getElementById('password');

    let isValid = true;

    if (!email.value || !/\S+@\S+\.\S+/.test(email.value)) {
      setEmailError(true);
      setEmailErrorMessage('Please enter a valid email address.');
      isValid = false;
    } else {
      setEmailError(false);
      setEmailErrorMessage('');
    }

    if (!password.value || password.value.length < 6) {
      setPasswordError(true);
      setPasswordErrorMessage('Password must be at least 6 characters long.');
      isValid = false;
    } else {
      setPasswordError(false);
      setPasswordErrorMessage('');
    }

    return isValid;
  };

  const handleSubmit = async (event) => {
    event.preventDefault();

    setFormErrorMessage(''); // Clear previous error

    if (!validateInputs()) {
      return;
    }

    const formData = new FormData();
    const email = document.getElementById('email').value;
    const password = document.getElementById('password').value;

    // Append email and password to FormData
    formData.append('email', email);
    formData.append('password', password);

    try {
      const response = await axios.post(`${API_BASE_URL}/login`, formData, {
        headers: {
          'Content-Type': 'multipart/form-data'
        }
      });
      if(response.data.status == 200){
        localStorage.setItem('token', response.data.token);
        navigate('/admin/categories');
      }else{
        setEmailError(true);
        setEmailErrorMessage(response.data.message);
        setFormErrorMessage(response.data.message || 'Login failed.'); // <-- Set form error
      }
    } catch (error) {
      // Handle error here (e.g., show error message)
      setFormErrorMessage('An error occurred while logging in. Please try again.'); // <-- Set error
      console.error('Login error:', error);
    }
  };

  return (
    <Box sx={{ minHeight: '100vh', bgcolor: 'background.default' }}>
      <CssBaseline enableColorScheme />
      <LoginContainer direction="column" justifyContent="space-between">
        <Card variant="outlined">
          <SitemarkIcon />
         {/* Show form error message if present */}
          {formErrorMessage && (
            <Typography color="error" sx={{ mb: 2, textAlign: 'center' }}>
              {formErrorMessage}
            </Typography>
          )}
          <Box
            component="form"
            onSubmit={handleSubmit}
            sx={{ display: 'flex', flexDirection: 'column', gap: 2 }}
          >
            <FormControl>
              <FormLabel htmlFor="email">Email</FormLabel>
              <TextField
                required
                fullWidth
                id="email"
                placeholder="your@email.com"
                name="email"
                autoComplete="email"
                variant="outlined"
                error={emailError}
                helperText={emailErrorMessage}
                color={emailError ? 'error' : 'primary'}
              />
            </FormControl>
            <FormControl>
              <FormLabel htmlFor="password">Password</FormLabel>
              <TextField
                required
                fullWidth
                name="password"
                placeholder="••••••"
                type="password"
                id="password"
                autoComplete="current-password"
                variant="outlined"
                error={passwordError}
                helperText={passwordErrorMessage}
                color={passwordError ? 'error' : 'primary'}
              />
            </FormControl>
          
            <Button
              type="submit"
              fullWidth
              variant="contained"
            >
              Sign in
            </Button>
          </Box>
         
        </Card>
      </LoginContainer>
    </Box>
  );
}